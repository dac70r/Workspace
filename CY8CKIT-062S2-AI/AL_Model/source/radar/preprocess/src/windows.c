void get_window(const sized_windows *sw, ifx_f32_t *out, uint16_t size)
{
    const ifx_f32_t *window;
    switch (size) 
    {
    case 16:
        window = sw->s16;
        break;
    case 32:
        window = sw->s32;
        break;
    case 64:
        window = sw->s64;
        break;
    case 128:
        window = sw->s128;
        break;
    case 256:
        window = sw->s256;
        break;
    default:
        abort();
    }

    ifx_f32_t sum = 0;
    for (int i = 0; i < size; ++i)
    {
        sum += window[i];
    }
    arm_scale_f32(
        (float32_t *)window, 1.0 / (float32_t)sum, (float32_t *)out, size
    );
}
